void suraj()
{

    int n;
    cin >> n;
    int k;
    cin >> k;

    string s;
    cin >> s;

    int cntB = 0;
    for (int i = 0; i < n; i++)
    {
        if (s[i] == 'B')
            cntB++;
    }


    if (cntB == k)
        cout << 0 << endl;
    else if (cntB < k)
    {
        int diff = k - cntB;
        int cntA = 0;
        int i = 0;
        for (i = 0; i < n; i++)
        {
            if (s[i] == 'A')
                cntA++;
            if (cntA == diff)
                break;
        }
        cout << 1 << endl;
        cout << i+1 << " "
             << "B" << endl;
    }
    else if (cntB > k)
    {
        if(k==0){
            cout<<1<<endl;
            cout<<n<<" A"<<endl;
        }else{
            int cntB =0;
            int i=n-1;
            for(i=n-1;i>=0;i--){
                if(s[i]=='B')cntB++;
                if(cntB==k)break;
            }
            for(int j=0;j<i;j++){
                if(s[j]=='B'){
                    cout<<1<<endl;
                    cout<<i<<" A"<<endl;
                    return;
                }
            }
            cout<<0<<endl;
        }
    }
}
